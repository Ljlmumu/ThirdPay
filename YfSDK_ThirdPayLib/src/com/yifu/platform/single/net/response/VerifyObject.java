package com.yifu.platform.single.net.response;

public class VerifyObject {
	private String verifyAdress;
	private String keyWord;
	private String keyLength;
	
	public VerifyObject(String verifyAdress, String keyWord, String keyLength) {
		super();
		this.verifyAdress = verifyAdress;
		this.keyWord = keyWord;
		this.keyLength = keyLength;
	}
	public String getVerifyAdress() {
		return verifyAdress;
	}
	public void setVerifyAdress(String verifyAdress) {
		this.verifyAdress = verifyAdress;
	}
	public String getKeyWord() {
		return keyWord;
	}
	public void setKeyWord(String keyWord) {
		this.keyWord = keyWord;
	}
	public int getKeyLength() {
		return Integer.parseInt(keyLength);
	}
	public void setKeyLength(String keyLength) {
		this.keyLength = keyLength;
	}
	
	
}
